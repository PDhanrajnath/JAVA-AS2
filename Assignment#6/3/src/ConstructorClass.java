// Creating a class
// name as a attribute
class ConstructorClass {

    public String name;

    // class constructor
    ConstructorClass(String name)
    {
        this.name = name;
    }

    // display() method to display
    // the student data
    public void display()
    {
        System.out.println("Name : " + name);
        System.out.println();
    }
}
